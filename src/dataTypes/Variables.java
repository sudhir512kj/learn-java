package dataTypes;

public class Variables {
    public static void main(String[] args) {
        // Range of byte is -128 to 127
        byte b = 5;
        short s = 300;
        int i = 1000;
        float f = 25.3f;
        char c = 'A';

        System.out.println("b = " + b);
        System.out.println("s = " + s);
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("c = " + c);
    }
}
