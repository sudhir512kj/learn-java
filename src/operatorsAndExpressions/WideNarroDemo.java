package operatorsAndExpressions;

public class WideNarroDemo {
    public static void main(String[] args) {
        byte b = 10; // 8 bits
        short s = 10; // 16 bits
        int i = 10; // 32 bits
        long l = 10L; // 64 bits, 'L' suffix indicates long type
        float f = 10.0f; // 32 bits, 'f' suffix indicates a float type
        double d = 10.0; // 64 bits, double is a double-precision floating-point number
        char c = 10; // 16 bits, char is a 16-bit Unicode character
        boolean bl = true; // 1 bit, boolean can be true or false
    }
}
